    public Glass(String manufacturer, String name, double d_index, double C, double F, double vd, double pd) {
        super(name,d_index,manufacturer);
        this.nC = C;
        this.nF = F;
        this.vd = vd;
        this.pd = pd;
        this._q = (nd - 1.) / vd;
        this._a = (vd * -0.001682) + 0.6438 + pd;
    }

    public Glass(double nd, double vd, double dpgf) {
        this(null,null,nd,0,0,vd,dpgf);
    }

    // The calculation below comes from GNUOptical
    public double compute_index_from_nd_vd(double wavelen) {
        double wl = wavelen / 1000.;
        double w2 = wl * wl;
        double w3 = w2 * wl;
        double f = (_a * -6.11873891971188577088 + 1.17752614766485175224)
                 + (_a * 18.27315722388047447566 + -8.93204522498095698779) / wl
                 + (_a * -14.55275321129051135927 + 7.91015964461522003148) / w2
                 + (_a * 3.48385106908642905310 + -1.80321117937358499361) / w3;
        return nd + _q * f;
    }

    @Override
    public double rindex(double wv_nm) {
        if (wv_nm == d) return nd;
        if (label != null) {
            if (wv_nm == C) return nC;
            else if (wv_nm == F) return nF;
            else
                throw new IllegalArgumentException("Glass does not have data for wavelength " + wv_nm);
        }
        else
            return compute_index_from_nd_vd(wv_nm);
    }

    @Override
    public StringBuilder toString(StringBuilder sb) {
        sb.append("Glass(").append("nd=").append(nd)
                .append(", vd=").append(vd);
        if (label != null)
                sb.append(", mat='").append(label).append("'");
        if (catalog_name != null)
                sb.append(", cat='").append(catalog_name).append("'");
        sb.append(")");
        return sb;
    }

